  /**
   * ë¬¸ìì—´ ì •ê·œí™” (ë„ì–´ì“°ê¸°, íŠ¹ìˆ˜ë¬¸ì ì œê±°)
   */
  normalizeString(str) {
    if (!str) return '';
    return str.toLowerCase()
      .replace(/\s+/g, '')           // ëª¨ë“  ê³µë°± ì œê±°
      .replace(/[\-\_\.]/g, '')      // í•˜ì´í”ˆ, ì–¸ë”ìŠ¤ì½”ì–´, ì  ì œê±°
      .replace(/[()]/g, '')          // ê´„í˜¸ ì œê±°
      .trim();
  }

  /**
   * ë‘ ë¬¸ìì—´ì˜ ìœ ì‚¬ë„ ê³„ì‚° (0~1)
   */
  calculateSimilarity(str1, str2) {
    if (!str1 || !str2) return 0;
    
    const s1 = this.normalizeString(str1);
    const s2 = this.normalizeString(str2);
    
    if (s1 === s2) return 1.0;
    
    // ê¸´ ë¬¸ìì—´ì—ì„œ ì§§ì€ ë¬¸ìì—´ì´ ì–¼ë§ˆë‚˜ í¬í•¨ë˜ëŠ”ì§€ ê³„ì‚°
    const [shorter, longer] = s1.length <= s2.length ? [s1, s2] : [s2, s1];
    
    if (shorter.length === 0) return 0;
    
    // ì™„ì „ í¬í•¨ ì²´í¬
    if (longer.includes(shorter)) {
      return shorter.length / longer.length;
    }
    
    return 0;
  }

  /**
   * ì¹´ì¹´ì˜¤ ê²€ìƒ‰ ê²°ê³¼ì—ì„œ ìµœì  ë§¤ì¹­ ì°¾ê¸°
   */
  findBestMatch(targetName, places) {
    let bestMatch = null;
    let bestScore = 0;

    for (const place of places) {
      const similarity = this.calculateSimilarity(targetName, place.place_name);
      
      // ìœ ì‚¬ë„ ì„ê³„ê°’ 0.7 ì´ìƒ
      if (similarity >= 0.7 && similarity > bestScore) {
        bestScore = similarity;
        bestMatch = {
          place: place,
          score: similarity
        };
      }
    }

    return bestMatch;
  }

